public class Hamburger {
    protected String name;
    protected String meat;
    protected String roll_type;
    protected float burger_price;

    public Hamburger(String name, String meat, String roll_type, float price) {
        this.name = name;
        this.meat = meat;
        this.roll_type = roll_type;
        this.burger_price = price;
    }
}
